public class Menu
{
    public void menu()
    {
        boolean salir = false;
        
        //Si salir es igual a false abandona el bucle while
        while (salir == false)
        {    //Declarar variable en 0 para iniciar el bucle escogiendo un numero
            int opcion = 0;
            
            
            //Estructura de desicion para continuar con el bucle
            if (opcion == 0)
            {
                break;
            }
        }
                
    }
}
